CREATE TABLE IF NOT EXISTS participants (
    id INT(6) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    firstName VARCHAR(50) NOT NULL,
    lastName VARCHAR(50) NOT NULL,
    email VARCHAR(250) UNIQUE KEY,
    position VARCHAR(30) DEFAULT 'novice',
    sharesAmount INT(10) UNSIGNED DEFAULT 0,
    startDate TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    parentId INT(6) UNSIGNED NOT NULL
)